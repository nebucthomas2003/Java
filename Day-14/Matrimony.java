public class Matrimony {
    


    private String name;
    private String gender;
    private  int age;
 
    public void setname(String name){
       this.name=name;
    }
    public void setgender(String gender){
       this.gender=gender;
    }
    public void setage(int age){
       this.age=age;
    }
 
    public String getname(){
       return name;
    }
    public String getgender(){
       return gender;
    }
    public int getage(){
       return age;
    }
     
 }
 